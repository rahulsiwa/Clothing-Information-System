    private void sortBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sortBtnActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i < listData.size() - 1; i++) //to place the element in it's correct spot
        {
            int minPos = i;
            for (int j = i + 1; j < listData.size(); j++) { //to go through the unsorted array inorder for us to find the smallest element
                TableData one = listData.get(minPos);
                TableData second = listData.get(j);
                if (one.getPrice() > second.getPrice()) {
                    minPos = j;
                }
            }
            Collections.swap(listData, minPos, i);

        }

        addSortedData();
        
    }//GEN-LAST:event_sortBtnActionPerformed

