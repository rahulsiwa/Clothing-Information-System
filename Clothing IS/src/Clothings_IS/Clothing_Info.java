    public static int binarySearch(ArrayList<TableData> list, int low, int high, int value) {
        if (low <= high) {
            int mid = (low + high) / 2;
            if (list.get(mid).getPrice() == value) {//list.get(mid).getPhoneNumber().compareTo(value) ==0
                return mid;
            } else if (list.get(mid).getPrice() > value) {//list.get(mid).getPhoneNumber().compareTo(value) >
                return binarySearch(list, low, mid - 1, value);
            } else {
                return binarySearch(list, mid + 1, high, value);
            }
        } else {
            return -1;
        }

    }

    public void addSortedData() {
        DefaultTableModel dtm = (DefaultTableModel) table.getModel();
        dtm.setRowCount(0);
        for (TableData td : listData) {
            String values[] = {td.getCode()+"",td.getBrands(),td.getCategory(),td.getPrice()+"",td.getQuantity()+"", td.getGender(), td.getSeason()};
            dtm.addRow(values);
        }

    }

    
    public void loadCSV(){
        File file = new File("Cloths.csv");
        try{
            if(!file.isFile()){ 
                file.createNewFile();
            }
            FileWriter writefile = new FileWriter(file);
            for(TableData td: listData){
                String values[] = {td.getCode()+"",td.getBrands(),td.getCategory(),td.getPrice()+"",td.getQuantity()+"", td.getGender(), td.getSeason()};
                for(int i=0;i<values.length;i++){
                    writefile.append(values[i]);
                    if(i==values.length-1){
                        break;
                    }
                    writefile.append(",");
                }
                writefile.append("\n");
                
            }
            writefile.flush();
            writefile.close();
        }
        catch(IOException ioe){
            JOptionPane.showMessageDialog(this, "IOException occured.");

        }
    }
